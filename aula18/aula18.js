// Diferença entre While e Do While:

// While = Primeiro faz o teste Lógico. Não tem certeza se vai ser executado.

// Do While = Executa e depois faz o teste Lógico. É executado pelo menos uma vez.

let n = 10;

while(n < 10){
    console.log('Curso CBF');
}



do{
    console.log('Curso CBF');
}while(n < 10);

console.log('Fim do Programa!');
